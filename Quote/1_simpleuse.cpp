# include <iostream>

using namespace std;

int main() {

    int a = 10; // 创建变量
    int &b = a; // 创建引用
    // 输出
    cout << "a = " << a << endl;
    cout << "b = " << b << endl;

    // 重新赋值
    b = 100;
    // 输出
    cout << "a = " << a << endl;
    cout << "b = " << b << endl;

    //int &c; // 错误，引用必须初始化
    int &c = a; // 一旦初始化后，就不可以更改
    c = b; // 这是赋值操作，不是更改引用
    cout << "a = " << a << endl;
    cout << "b = " << b << endl;
    cout << "c = " << c << endl;

    return 0;
}